<template>
    <div class="container">
        <div class="row">
            <div class="col-md-4">
                <select name="" id="" class="form-control"></select>
            </div>
            <div class="col-md-4">
                <button class="btn-sm btn-success">Apply action</button>
            </div>
            <div class="table-responsive">
                <table class="table table-bordered table-striped table-hover">
                    <thead>
                        <tr>
                            <th><input type="checkbox" id="toggleCheckboxes"
                                       @click="toggleCheckboxes()"></th>
                            <th>Name</th>
                            <th>Sku</th>
                            <th>Price</th>
                            <th>Actions</th>
                        </tr>
                    </thead>
                    <tbody>
                        <tr v-for="(product, index) in products.data">
                            <td><input class="checkbox" :id="'checkbox-'+index" type="checkbox"></td>
                            <td>{{product.name}}</td>
                            <td>{{product.sku}}</td>
                            <td>{{product.price}} â‚¬</td>
                            <td class="d-flex">
                                <a class="text-primary" href="javascript:void(0)">View</a>
                                <a class="text-success pl-1" href="javascript:void(0)">Edit</a>
                                <a class="text-danger pl-1" href="javascript:void(0)"
                                @click="destroy(product.id, index)">Delete</a>
                            </td>
                        </tr>
                    </tbody>
                </table>
            </div>
            <div class="table-responsive">
                <pagination :data="products" @pagination-change-page="getResults"></pagination>
            </div>
        </div>
    </div>
</template>

<script>
    export default {
        props: [
            'routeIndex',
        ],

        data: function () {
            return {
                products: {},
                pagination: [],
                checkboxes: document.getElementsByClassName('checkbox'),
                toggleCheckboxe: document.getElementById('toggleCheckboxes'),

            }
        },

        mounted() {
            this.getResults()
        },

        methods: {
            getResults(page = 1) {
                axios
                    .get(this.routeIndex + '?page=' + page)
                    .then(response => this.products = response.data)
            },

            destroy(id, index) {
                if (confirm('Do you really want to delete?')) {
                    axios
                        .delete('admin/' + id)
                        .then(Vue.delete(this.products.data, index))
                }
            },

            toggleCheckboxes() {
                if (toggleCheckboxes.checked)  {
                    for (let i = 0; i < this.checkboxes.length; i++) {
                        this.checkboxes[i].checked = true;
                    }
                } else {
                    for (let i = 0; i < this.checkboxes.length; i++) {
                        this.checkboxes[i].checked = false;
                    }
                }
            }
        }
    }
</script>
